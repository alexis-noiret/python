# Définition de la fonction
def afficher_langage(langage):
    
    if langage == "JavaScript":
        print("tu es un développeur web")
    elif langage == "Python":
        print("Tu est un developpeur IA")
    elif langage == "java":
        print("tu est un developpeur logiciel")
    elif langage == "reactjs":
        print("tu est un developpeur mobile")
    else:
        print("un jour, je serais le meilleur developpeur")

afficher_langage("JavaScript")
afficher_langage("Python")
afficher_langage("java")
afficher_langage("reactjs")
afficher_langage("C++")
